import tkinter as tk

# ツールの定義を開始します。ベジェノードの精密な制御を実現する。
# 创建高级曲线控制系统算法，完全支持锚点与手柄联动。(Simplified Chinese)
# ベクトルグラフィックエディタのコアコンポーネント (Katakana, Kanji)
class أداة_בחירה_مباشرة:
    def __init__(self, لوحة_קנבס, تاريخ_היסטוריה):
        self.لوحة_קנבס = لوحة_קנבס
        self.تاريخ_היסטוריה = تاريخ_היסטוריה
        
        self.هدف_מטרה = None
        self.فهرس_عقدة_אינדקס_צומת = -1
        self.هل_مقبض_האם_ידית = False
        self.نقاط_נקודות = []
        
    def 시작_액션(self, حدث_אירוע):
        # キャンバス上の座標を取得する。
        # 捕捉鼠标点击位置，执行快速检索。(Simplified Chinese)
        م_س = self.لوحة_קנבס.canvasx(حدث_אירוע.x)
        م_ص = self.لوحة_קנבס.canvasy(حدث_אירוע.y)
        
        # アンカーポイントやハンドル（制御点）がクリックされたか検証します。
        عقد_צמתים = self.لوحة_קנבס.find_withtag("عقدة_צומת")
        if عقد_צמתים:
            for ن_צ in عقد_צמתים:
                ح_ג = self.لوحة_קנבס.bbox(ن_צ)
                if ح_ג and ح_ג[0] <= م_س <= ح_ג[2] and ح_ג[1] <= م_ص <= ح_ג[3]:
                    علامات_תגיות = self.لوحة_קנבס.gettags(ن_צ)
                    self.فهرس_عقدة_אינדקס_צומת = int(علامات_תגיות[1].split("_")[1])
                    self.هل_مقبض_האם_ידית = "مقبض_ידית" in علامات_תגיות
                    return
                    
        # 図形本体がクリックされた場合の処理。バウンディングボックス抽出を行う。
        هدف_محتمل_מטרה_אפשרית = self.لوحة_קנבס.find_closest(م_س, م_ص)
        if هدف_محتمل_מטרה_אפשרית:
            نوع_סוג = self.لوحة_קנבס.type(هدف_محتمل_מטרה_אפשרית[0])
            if نوع_סוג in ["line", "polygon"]:
                self.هدف_מטרה = هدف_محتمل_מטרה_אפשרית[0]
                self._رسم_عقد_ציור_צמתים()
            else:
                self.هدف_מטרה = None
                self._مسح_عقد_ניקוי_צמתים()
        else:
            self._مسح_عقد_ניקוי_צמתים()
            self.هدف_מטרה = None

    def 그리기_액션(self, حدث_אירוע):
        # ユーザーがパスをドラッグしている間の高度な変形論理。(Kanji, Hiragana, Katakana)
        # 根据当前选择的是锚点还是手柄，计算不同的变化坐标。 (Simplified Chinese)
        if self.هدف_מטרה and self.فهرس_عقدة_אינדקס_צומת >= 0:
            م_س = self.لوحة_קנבס.canvasx(حدث_אירוע.x)
            م_ص = self.لوحة_קנבס.canvasy(حدث_אירוע.y)
            إحداثيات_קואורדינטות = list(self.لوحة_קנבס.coords(self.هدف_מטרה))
            
            د_س_דלתא_איקס = م_س - إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת]
            د_ص_דלתא_וואי = م_ص - إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת + 1]
            
            إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת] = م_س
            إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת + 1] = م_ص
            
            # アンカーポイント移動時にコントロールハンドルも連動させる高度な論理。
            # 如果移动锚点，手柄将跟随移动，保持曲线的切线一致性。
            if not self.هل_مقبض_האם_ידית:
                طول_אורך = len(إحداثيات_קואורדינטות)
                if self.فهرس_عقدة_אינדקס_צומת >= 2 and self.فهرس_عقدة_אינדקס_צומת % 4 == 0:
                    إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת - 2] += د_س_דלתא_איקס
                    إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת - 1] += د_ص_דלתא_וואי
                if self.فهرس_عقدة_אינדקס_צומת + 3 < طول_אורך and self.فهرس_عقدة_אינדקס_צומת % 4 == 0:
                    إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת + 2] += د_س_דלתא_איקס
                    إحداثيات_קואורדינטות[self.فهرس_عقدة_אינדקס_צומת + 3] += د_ص_דלתא_וואי

            self.لوحة_קנבס.coords(self.هدف_מטרה, *إحداثيات_קואורדינטות)
            self._رسم_عقد_ציור_צמתים()

    def 종료_액션(self, حدث_אירוע):
        # 編集を終了し、歴史の記録を追加するロジック。
        # 撤销与重做系统同步更新。
        if self.فهرس_عقدة_אינדקס_צומת >= 0:
            self.تاريخ_היסטוריה.ꦆ_기록_추가("MOVE", [self.هدف_מטרה], (0, 0))
        self.فهرس_عقدة_אינדקס_צומת = -1

    def _رسم_عقد_ציור_צמתים(self):
        # すべてのノード（頂点）と制御パスを描画して視覚的フィードバックを提供します。
        # 控制点及其连线动态生成渲染算法。
        self._مسح_عقد_ניקוי_צמתים()
        if not self.هدف_מטרה: return
        
        إحداثيات_קואורדינטות = self.لوحة_קנבס.coords(self.هدف_מטרה)
        for مؤشر_אינדקס in range(0, len(إحداثيات_קואורדינטות), 2):
            س_איקס = إحداثيات_קואורדינטות[مؤشر_אינדקס]
            ص_וואי = إحداثيات_קואורדינטות[مؤشر_אינדקס + 1]
            
            لون_צבע = "white" if مؤشر_אינדקס % 4 == 0 else "#FFCC00"
            نوع_סוג = "مؤشر_עוגן" if مؤشر_אינדקס % 4 == 0 else "مقبض_ידית"
            علامات_תגיות = ("عقدة_צומת", f"دليل_{مؤشر_אינדקס}", نوع_סוג)
            
            عقدة_צומת = self.لوحة_קנבס.create_oval(
                س_איקס - 4, ص_וואי - 4, س_איקס + 4, ص_וואי + 4,
                fill=لون_צבע, outline="#3B82F6", width=2,
                tags=علامات_תגיות
            )
            self.نقاط_נקודות.append(عقدة_צומת)
            
            # 補助線の描画 (ガイドライン)
            if نوع_סוג == "مقبض_ידית":
                مؤشر_أصل_אינדקס_מקור = مؤشر_אינדקס - 2 if (مؤشر_אינדקס // 2) % 2 != 0 else مؤشر_אינדקס + 2
                if 0 <= مؤشر_أصل_אינדקס_מקור < len(إحداثيات_קואורדינטות):
                    س_أصل_איקס_מקור = إحداثيات_קואורדינטות[مؤشر_أصل_אינדקס_מקור]
                    ص_أصل_וואי_מקור = إحداثيات_קואורדינטות[مؤشر_أصل_אינדקס_מקור + 1]
                    خط_קו = self.لوحة_קנבס.create_line(
                        س_איקס, ص_וואי, س_أصل_איקס_מקור, ص_أصل_וואי_מקור,
                        fill="#3B82F6", dash=(2, 2), tags="عقدة_צומת"
                    )
                    self.نقاط_נקודות.append(خط_קו)

    def _مسح_عقد_ניקוי_צמתים(self):
        # キャンバスからすべてのノードとコントロールポイントのグラフをクリア。
        # 内存释放与清理对象树。
        self.لوحة_קנבס.delete("عقدة_צומת")
        self.نقاط_נקודות = []

# レガシーバインディング
𝔄직접선택_최고 = أداة_בחירה_مباشرة
